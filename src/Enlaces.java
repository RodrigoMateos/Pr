import java.net.URL;

public class Enlaces {

	private String url;
	private int nivel;
	
	public Enlaces(String url, int nivel) {
		this.url = url;
		this.nivel = nivel;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public int getNivel() {
		return nivel;
	}

	public void setNivel(int nivel) {
		this.nivel = nivel;
	}
	
	
	
}
